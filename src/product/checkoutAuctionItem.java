package product;

public class checkoutAuctionItem {
	private int checkoutAuctionId;
	private String BuyerId;
	private int auctionId;
	private int price;
	private int numberOfProduct;
	private int orderId;
	private String status;
	private String picturePath;
	private String sellerId;
	private String name;
	
	public int getCheckoutAuctionId() {
		return checkoutAuctionId;
	}
	public void setCheckoutAuctionId(int checkoutAuctionId) {
		this.checkoutAuctionId = checkoutAuctionId;
	}
	public String getBuyerId() {
		return BuyerId;
	}
	public void setBuyerId(String buyerId) {
		BuyerId = buyerId;
	}
	public int getAuctionId() {
		return auctionId;
	}
	public void setAuctionId(int auctionId) {
		this.auctionId = auctionId;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getNumberOfProduct() {
		return numberOfProduct;
	}
	public void setNumberOfProduct(int numberOfProduct) {
		this.numberOfProduct = numberOfProduct;
	}
	public int getOrderId() {
		return orderId;
	}
	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPicturePath() {
		return picturePath;
	}
	public void setPicturePath(String picturePath) {
		this.picturePath = picturePath;
	}
	public String getSellerId() {
		return sellerId;
	}
	public void setSellerId(String sellerId) {
		this.sellerId = sellerId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
